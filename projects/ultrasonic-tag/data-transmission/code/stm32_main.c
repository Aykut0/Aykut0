void DWT_Init(void)
{
  // Enable DWT access
  CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
  // Clear DWT control register
  DWT->CTRL &= ~DWT_CTRL_CYCCNTENA_Msk;
  // Reset the cycle counter
  DWT->CYCCNT = 0;
  // Enable cycle counter
  DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;
}

